package ex1;

/**
 * Classe de calcul élémentaires
 * 
 * @author Patrice
 *
 */
public class Calcul {

	/**
	 * Addition des nombres a et b
	 * 
	 * @param a
	 * @param b
	 * @return résultat d'addition
	 */
	public int addition(int a, int b) {
		return a + b;
	}

	/**
	 * Soustraction des nombre a et b
	 * 
	 * @param a
	 * @param b
	 * @return réusultat d'addition
	 */
	public int soustraction(int a, int b) {
		return a - b;
	}
}
